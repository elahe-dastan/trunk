package normalize

import (
	"log"
	"regexp"
)

func number(words []string) []string {
	re, err := regexp.Compile("[۰-۹]+|[0-9]+")
	if err != nil {
		log.Fatal(err)
	}

	ans := make([]string, 0)
	for _, word := range words {
		if !re.MatchString(word) {
			ans = append(ans, word)
		}
	}

	return ans
}

func singleChar(words []string) []string {
	ans := make([]string, 0)
	for _, word := range words {
		if len([]rune(word)) > 1 {
			ans = append(ans, word)
		}
	}

	return ans
}

// ZWNJ and ZWSP
func zeroWidth(words []string) []string {
	ans := make([]string, 0)
	for _, word := range words {
		normalizedWord := make([]rune, 0)
		for _, r := range []rune(word) {
			if r != '‌' && r != '​' && r != '­' {
				normalizedWord = append(normalizedWord, r)
			}
		}
		ans = append(ans, string(normalizedWord))
	}

	return ans
}

func ha(words []string) []string {
	ans := make([]string, 0)
	for _, word := range words {
		// ها
		runes := []rune(word)
		if string(runes[len(runes)-2:]) == "ها" {
			if string(runes[:len(runes)-2]) != "تن" {
				word = string(runes[:len(runes)-2])
			}
		}
		if word != "" {
			ans = append(ans, word)
		}
	}
	return ans
}

func tarin(words []string) []string {
	ans := make([]string, 0)
	for _, word := range words {
		// ترین
		runes := []rune(word)
		if len(runes) < 4 {
			ans = append(ans, word)
			continue
		}
		if string(runes[len(runes)-4:]) == "ترین" {
			word = string(runes[:len(runes)-4])

		}
		if word != "" {
			ans = append(ans, word)
		}
	}
	//	ends = ['ات', 'ان', 'ترین', 'تر', 'م', 'ت', 'ش', 'یی', 'ی', 'ها', 'ٔ', '‌ا',]
	return ans
}

func arabicToPersian(words []string) []string {
	m := map[rune]rune{'ك': 'ک', 'ى': 'ی', 'ي': 'ی'}
	ans := make([]string, 0)
	for _, word := range words {
		runes := []rune(word)
		for i, r := range runes{
			p, ok := m[r]
			if ok{
				runes[i] = p
			}
		}
		ans = append(ans, string(runes))
	}
	return ans
}

